/*
  Operadores de atribuição (assignment)
  
  --> atribuição simples
  --> atribuição com operadores aritméticos
*/

let x // declarando uma variável
console.log(x) // a variável existe mas nada foi atribuido a ela

x = 1 // atribuindo a x o valor numérico 1
console.log(x) 

x += 2 // atribuição com operador aritmético "+" --> equivale a x = x + 2
console.log(x)

x -= 1 // atribuição com operador aritmético "-" --> equivale a x = x - 1
console.log(x)

x *= 5 // atribuição com operador aritmético "*" --> equivale a x = x * 5
console.log(x)

x /= 2 // atribuição com operador aritmético "/" --> equivale a x = x / 2
console.log(x)

x %= 3 // atribuição com operador aritmético "%" --> equivale a x = x % 3
console.log(x)

x **= 3 // atribuição com operador aritmético "**" --> equivale a x = x ** 3
console.log(x)