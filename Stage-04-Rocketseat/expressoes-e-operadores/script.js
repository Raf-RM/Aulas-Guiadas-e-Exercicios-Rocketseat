/*
  operadores aritiméticos

  * soma +
  * subtração -
  * multiplicação *
  * divisão /
  * resto da divisão %
  * incremento ++
  * decremento --
  * exponencial
*/
let valor = 0

console.log(56 + 3) // soma
console.log(18 - 5) //subtração
console.log(2.5 * 4) // multiplicação
console.log(6 / 3) // divisão
console.log(19 % 4) // resto da divisão inteiro

console.log(valor)
console.log(++valor) // incremento dentro do console.log sendo incrementado antes
console.log(valor++) // incremento dentro do console.log sendo incrementado depois
console.log(valor) // mostrando o incremento da última linha
valor++ // incremento fora do console.log()
console.log(valor)

console.log(--valor) // decremento dentro do console.log sendo feito antes
console.log(valor--) // decremento dentro do console.log sendo feito depois
console.log(valor) // mostrando o decremento da linha anterior
valor-- // decremento fora do console.log
console.log(valor)

console.log(3 ** 2) // exponencial 3 elevado a segunda potencia

