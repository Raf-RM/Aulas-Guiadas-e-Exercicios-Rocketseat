/*
   variáveis
   --> nomes simbolicos para receber algum valor
   --> atalhos de codigo
   --> identificadores
   --> para criar uma variável temos 3 palavras reservadas:
      * var
      * let
      * const
*/

var clima = "quente"
clima = "frio"
console.log(clima)

let wether = "warm"
wether = "hot"
console.log(wether)

const season = "spring"
// season = "summer" se fizermos isso teremos um erro pois
//estamos atribuindo outro valor a uma constante
console.log(season)

